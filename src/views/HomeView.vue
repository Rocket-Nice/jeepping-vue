<script>

export default {
  data() {
    return {
      tabs: [
        { id: 'day-tours', title: 'Day Tours' },
        { id: 'weekend-tour', title: 'Weekend Tour' },
        { id: 'multi-day-tours', title: 'Multi-day Tours' }
      ],
      activeTab: 'day-tours',
      isActive: false,
    };
  },
  methods: {
    toggleTab(tabId) {
      this.activeTab = tabId;
      const blockElements = this.$refs.blockElements; // Получаем ссылку на блок tours-list__block-elements
      const elements = blockElements.getElementsByClassName('tab-container'); // Находим все элементы с классом tab-container

      // Проходимся по каждому элементу и проверяем его id
      Array.from(elements).forEach(element => {
        if (element.id === tabId) {
          console.log("Выбран существующий tabId:", tabId);
          element.classList.add("show-tab"); // Добавляем класс "show-tab" к выбранному элементу
        } else {
          element.classList.remove("show-tab"); // Удаляем класс "show-tab" у других элементов
        }
      });
    },
  },
};
</script>

